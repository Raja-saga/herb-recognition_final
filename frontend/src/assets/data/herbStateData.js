// Normalize herb names: underscore to space
const normalizeHerbName = (herbName) => {
  return herbName.replace(/_/g, ' ');
};

// Get enhanced state data with full coverage
export const getHerbStateData = (predictedHerb) => {
  if (!predictedHerb) return null;
  
  const normalizedName = normalizeHerbName(predictedHerb);
  const baseData = herbStateData[normalizedName];
  
  if (!baseData) return null;
  
  const enhanced = enhanceDatasetWithFullCoverage(baseData.states);
  return {
    ...enhanced,
    ecology: baseData.ecology,
    herb: normalizedName
  };
};
